<?php
//o operador ++ ou -- não precisa de uma estrutura de repetição para usalo


$valor1 = 0;
$valor2 = 0;


$valor1 ++; //aqui da o mesmo resultado

$valor2 = $valor2 + 1; //não compensa usar dessa forma usa mais codio

echo "Valor 1: $valor1";
echo "<br>";
echo "Valor 2: $valor2";

echo "<br>";
echo "<br>";

//decremento --

$n = 10;
$m = 10;

$n --;
$m = $m -1;

echo "Valor 1: $n";
echo "<br>";
echo "Valor 2: $m";





?>